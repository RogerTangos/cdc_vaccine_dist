private void managejTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_managejTableMouseClicked
// TODO add your handling code here:
    //make sure ua is empty, or it will give a null pointer.

    int selectedRow = managejTable.getSelectedRow();
    if (selectedRow < 0 || selectedRow > managejTable.getRowCount()) {
        return;
    }
    
    cdc = (CDC) managejTable.getValueAt(selectedRow, 0);
            
    namejTextField.setText(cdc.getOrgName());
    staffjTextField.setText(Integer.toString(cdc.getStaffDirectory().getStaffList().size()));
    add1jTextField.setText(cdc.getLocation().getAddress1());
    add2jTextField.setText(cdc.getLocation().getAddress2());
    cityjTextField.setText(cdc.getLocation().getCity());
    statejTextField.setText(cdc.getLocation().getState());
    zipjTextField.setText(cdc.getLocation().getPostal());
    countryjTextField.setText(cdc.getLocation().getCountry());

    refresh();
    refreshStaffTable();



}//GEN-LAST:event_managejTableMouseClicked

private void updatejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatejButtonActionPerformed
// TODO add your handling code here:
    if (cdc != null) {
       
        Location l = cdc.getLocation();
        
        cdc.setOrgName(namejTextField.getText());
        l.setAddress1(add1jTextField.getText());
        l.setAddress2(add2jTextField.getText());
        l.setCity(cityjTextField.getText());
        l.setState(statejTextField.getText());
        l.setPostal(zipjTextField.getText());
        l.setCountry(countryjTextField.getText());
        
        refresh();
   
    }
}//GEN-LAST:event_updatejButtonActionPerformed

