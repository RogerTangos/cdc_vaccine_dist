    void refresh() {
        int rowcount = managejTable.getRowCount();
        int i;
        for (i = rowcount - 1; i >= 0; i--) {
            ((DefaultTableModel) managejTable.getModel()).removeRow(i);
        }
        
        Network network = (Network) enterprise;
        
        ArrayList<Enterprise> clist = network.getEnterpriseList();
        for (Enterprise c : clist) {
            if(c instanceof CDC){
            Object[] col = new Object[5];
            col[0] = c;
            col[1] = c.getStaffDirectory().getStaffList().size();
            col[2] = c.getLocation().getAddress1();
            col[3] = c.getLocation().getCity();
            col[4] = c.getLocation().getState();

            ((DefaultTableModel) managejTable.getModel()).addRow(col);
        }
        }
    }
    
    void refreshStaffTable(){
        int rowcount = staffjTable.getRowCount();
        int i;
        for (i = rowcount - 1; i >= 0; i--) {
            ((DefaultTableModel) staffjTable.getModel()).removeRow(i);
        }
        
        ArrayList<UserAccount> userAccount = cdc.getUserAccountDirectory().getUserAccountList();
        for (UserAccount o : userAccount) {
            Object[] col = new Object[1];
            col[0] = o;

            ((DefaultTableModel) staffjTable.getModel()).addRow(col);
        }
    }
    

