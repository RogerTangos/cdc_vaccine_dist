private void addUserAccountjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUserAccountjButtonActionPerformed
// TODO add your handling code here:
        Person tempPerson = enterprise.getPersonDirectory().newPerson();        
        
        String firstName = firstjTextField.getText();
        String lastName = lastNamejTextField.getText();
        String dob = dobjTextField.getText();
        String ssn = ssnjTextField1.getText();

        tempPerson.setFirstName(firstName);
        tempPerson.setLastName(lastName);
        tempPerson.setDob(dob);
        tempPerson.setSsn(ssn);
        
        JOptionPane.showMessageDialog(null, "Person Added");
        
}//GEN-LAST:event_addUserAccountjButtonActionPerformed

