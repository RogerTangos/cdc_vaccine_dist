    void refresh() {
        int rowcount = managejTable.getRowCount();
        int i;
        for (i = rowcount - 1; i >= 0; i--) {
            ((DefaultTableModel) managejTable.getModel()).removeRow(i);
        }

        Map map = (Map) e.getWorkRequestList(); //cast the HashMap to map
        Set set = map.entrySet(); //and get the entry set

        Iterator iter = set.iterator(); //get the number of entries

        while (iter.hasNext()) {
            Map.Entry entry = (Map.Entry) iter.next();
            ArrayList<WorkRequest> entryRequests = (ArrayList<WorkRequest>) entry.getValue();
            for (WorkRequest c : entryRequests) {
                if (c.getInvoice() != null && c.getInvoice().getPayment().equals("unpaid") && c.getInvoice().getStatus().equals("payable")) {
                    Object[] col = new Object[7];

                    Vaccine vaccine = c.getInventory().getVaccine();
                    double vaccinePricePer = vaccine.getPrice();
                    int count = c.getInventory().getCount();

                    col[0] = vaccine;
                    col[1] = count;
                    col[2] = vaccinePricePer;
                    col[3] = count * vaccinePricePer;
                    col[4] = c.getInvoice().getOwedTo();
                    col[5] = c.getEditorEnterprise().getOrgName();
                    col[6] = c;

                    ((DefaultTableModel) managejTable.getModel()).addRow(col);
                }
            }
        }
    }

    void refreshInventory() {
        int rowcount = inventoryjTable.getRowCount();
        int i;
        for (i = rowcount - 1; i >= 0; i--) {
            ((DefaultTableModel) inventoryjTable.getModel()).removeRow(i);
        }
        Map map = (Map) e.getWorkRequestList(); //cast the HashMap to map
        Set set = map.entrySet(); //and get the entry set

        Iterator iter = set.iterator(); //get the number of entries

        while (iter.hasNext()) {
            Map.Entry entry = (Map.Entry) iter.next();
            ArrayList<WorkRequest> entryRequests = (ArrayList<WorkRequest>) entry.getValue();

            for (WorkRequest c : entryRequests) {
                if (c.getCount() == WR.getCount() && c.getInvoice()==null) {
                    Object[] col = new Object[6];
                    Inventory inventory = c.getInventory();
                    Vaccine vaccine = inventory.getVaccine();
                    
                    col[0] = vaccine.getName();
                    col[1] = inventory.getCount();
                    col[2] = inventory.getSerialNo();
                    col[3] = vaccine.getLifespan();
                    col[4] = vaccine.getDisease();
                    col[5] = c.getOriginator();


                    ((DefaultTableModel) inventoryjTable.getModel()).addRow(col);
                }
            }
        }
    }

