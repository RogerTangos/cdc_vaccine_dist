private void managejTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_managejTableMouseClicked
// TODO add your handling code here:
    int selectedRow = managejTable.getSelectedRow();
    if (selectedRow < 0 || selectedRow > managejTable.getRowCount()) {
        return;
    }

    WR = (WorkRequest) managejTable.getValueAt(selectedRow, 6);

    Inventory inventory = WR.getInventory();
    Vaccine vaccine = inventory.getVaccine();
    Invoice invoice = WR.getInvoice();

    //there is a bug here because i can't display the wr name.

    vaccinejLabel1.setText(vaccine.getName());
    diseasejLabel1.setText(vaccine.getDisease().getName());
    orderedjLabel1.setText(Integer.toString(invoice.getCount()));
    priceLabel1.setText(Double.toString(vaccine.getPrice()));

    //this panel is built for PHDs, CDCs, and Providers, so I have to show the correct one.
    if (e instanceof Provider) {
        percentjLabel1.setText(Double.toString(vaccine.getProviderPercent()) + "%");
    }

    if (e instanceof PHD) {
        percentjLabel1.setText(Double.toString(vaccine.getPhdPercent()) + "%");
    }

    if (e instanceof CDC) {
        percentjLabel1.setText(Double.toString(vaccine.getCdcPercent()) + "%");
    }

    manufacturerjLabel1.setText(inventory.getManufacturer().getOrgName());
    dateMadejLabel1.setText(DateFormat.getInstance().format(inventory.getDateCreated()));
    expirationjLabel1.setText(DateFormat.getInstance().format(inventory.getDateExpired()));
    
    if(inventory.getWarehouse()!=null){
    warehousejLabel2.setText(inventory.getWarehouse().getOrgName());
    }else{
        warehousejLabel2.setText("not assigned");
    }
    
    invoiceDateLabel1.setText(DateFormat.getInstance().format(invoice.getCreationDate()));
    payableTojLabel1.setText(invoice.getOwedTo().getOrgName() + " " + invoice.getOwedTo().getClass().getSimpleName());
    invoiceByjLabel1.setText(WR.getCreator().getUserName());
    totalOwedjLabel1.setText("$" + Double.toString(invoice.getAmount()));

    paymentOwedjLabel1.setText(WR.getInvoice().getPayment());


    refreshInventory();

}//GEN-LAST:event_managejTableMouseClicked

